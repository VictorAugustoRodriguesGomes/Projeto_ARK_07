    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        t2 = texto.getText();
        n2 = Float.parseFloat(t2);
        if (sinal == 1) {
            n3 = n1 + n2;
            t2 = String.valueOf(n3);
            texto.setText(t2);
        }
        if (sinal == 2) {
            n3 = n1 - n2;
            t2 = String.valueOf(n3);
            texto.setText(t2);
        }
        if (sinal == 3) {
            n3 = n1 * n2;
            t2 = String.valueOf(n3);
            texto.setText(t2);
        }
        if (sinal == 4) {
            n3 = n1 / n2;
            t2 = String.valueOf(n3);
            texto.setText(t2);
        }
    }//GEN-LAST:event_jButton14ActionPerformed

